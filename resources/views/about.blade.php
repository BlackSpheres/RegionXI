				    	<h2 data-center="opacity: 1"  data-206-top="opacity: 1" data-anchor-target="#ethnic h2">ETHNIC GROUPS</h2>
				    	<hr>
				    	<blockquote>
				    	<p data-center="opacity: 1"  data-206-top="opacity: 1"  data-anchor-target="#culture h2">
				    	<mark>The Atas</mark> (also Ataas, Itaas) are powerful people of unknown origin who appear to be a <b>mixture of Negritos and Malays</b>. They occupy the hinterlands comprising the headwaters of Davao, Tuganay and Libuganon Rivers. They are very sensitive but can easily be put under control if done with tact. A vindictive type of people, utmost care is exercised in dealing with them. They have a language of their own. Their name means “dwellers in highlands”. Living in frail huts of palm leaves and bark of trees built on top of trees, they depend primarily on hunting for their livelihood. They are believed to be <mark>the most numerous</mark>.<br><br>

				    	<mark>The Bagobos</mark> mostly occupy the lower slopes of Mt. Apo, extending from upper Digos in the south to Talomo and Tugbok in the north and to Baguio in upper Calinan on the northwest slope. They are known as <mark>the most elaborately dressed</mark> of the Davao tribes. In view of their proximity to the Christian settlements since Spanish times, they are the tribe most integrated with Christian society. The influx of newcomers to Davao, starting with the Japanese, has gradually eased this tribe from the excellent lands they had been occupying.<br><br>

				    	<mark>The B’la-ans</mark> are the Malay people occupying mainly the interior region between the Bulatukan River (in Bansalan) and the Sarangani Islands and it’s Bay. Exceedingly timid, they have a peaceful disposition and are <mark>the most industrious</mark> of all the natives. They grow rice on the plains of the interior region they occupy.<br><br>

				    	<mark>The Dulanganes</mark> (Gulanganes, also called Bangai-Bangal by the Moros) are found in the hinterlands of southern Davao and Cotabato. It is not known whether they are pure-blooded or Malay with an infusion of Negrito blood. <mark>They are savage and fierce</mark>, and Moros themselves do not want to meddle with then, calling them <b>a bad race</b>. They are naked except for a small covering made of leaves or bark of trees. They have no houses and Iive in caves or inside tree trunks. Their weapons usually are poisoned arrows.<br><br>

						<mark>The Guiangas</mark> are scattered on the Rancherias of Gumalang, Tamugan, upper Toril and Biao. The tribe’s dialect is totally different from neighboring Bagobo’s. They are also called Guanga or Guianga, which means “forest people.” They are suspected of being fragments of the little-known tribe, who according to location lived in southern Mindanao under the names Manguangas, Mangulangas or Dulanganes. Like Bagobos, they practice human sacrifice.<br><br>

						<mark>The Loacs</mark> belong to the Tagacaolo tribe who dwells in the mountain forest of the San Agustin peninsula. They are <mark>the poor members</mark> of the Tagacaolo tribe who have isolated themselves as a means of protection from being made slaves by the rich and powerful segment of their tribe. <br><br>

						<mark>The Maguindanao</mark> are <b>the Moros of Cotabato</b>, also the Moros who inhabit the Sarangani Islands and parts of Davao Gulf coast. Living mostly along the mouths of rivers, they impeded the Spanish colonizers’ efforts at bringing the other tribes of Davao within the Christian fold.<br><br>

						<mark>The Mandayas</mark> (Mandaya or “people of the upland”, Ilaya) are of Malay stock. They inhabit the slopes of the mountain range that borders the Pacific Ocean, from Mati to Bislig, and the area in upper Tagum and Hijo Rivers, as well as the upper Agusan River Valley. They were famed as a headhunting people, but their early contacts with the Spanish colonizers (since early 1600s) have made them <mark>the first tribe to embrace civilized life</mark>. <br><br>

						<mark>The Manobos</mark> (Manuba or Man-Suba, ‘river people”, also “those who grew up” (of Malay extraction) chiefly occupy the Agusan river valley in Compostela. They inhabit various points from Malalag to Sarangani and between Cuabo and Cape San Agustin. The Manobos are considered <mark>the most aboriginal tribe</mark> of Mindanao. Of Malay stock, the Manobos are slight of built but athletic. They have little liking for work and are warlike and valiant, being usually hunters for slaves. Although wild, they are easy to resettle but difficult to preserve. Their houses are built near the rivers, often on the forks of trees. They change glass-stringed beads, Manobos prefer black rather than the more popular red among the Mandayas. <br><br>

						<mark>The Samales</mark> inhabit the island of Samal in the Davao Gulf. They are mostly Moro-Mandaya mestizos. Originally believed to be the descendants of that emigration (between 1460-1480) led by the Shariff Kabunsuwan from Johore, Malaya, who were dispersed by astorm, they reportedly found their way to different islands south of the Philippines, including Mindanao. Tradition distinctly states that he people who came with Kabungsuwan were Samales. The Samales or Bajaws are the sea nomads of the Malay Archipelago, and their emigrations are frequent. They are not so difficult to resettle, and are in fact well-inclined to the Spaniards whom they helped in ousting the Moro chieftain.<br><br></p></blockquote>
