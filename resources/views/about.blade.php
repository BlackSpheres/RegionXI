				    	<blockquote>
				    	<h3 style="font-family: Ikaros; color: black; size: 70px;">DEMOGRAPHICS</h3>
				    	<blockquote  style="text-align: justify;">
				    	
				    	<p data-center="opacity: 1"  data-206-top="opacity: 1"  data-anchor-target="#culture h2">
				    		<b>Davao Region</b> is <mark>the most populous region in Mindanao </mark> and the <b>7th most populous in the country</b>, with a total of 4,893,318 inhabitants in 2015. <b>Davao City</b>, its regional capital, is also <mark>the largest city in Mindanao</mark>, with an area of 2,444 km2, the largest in the country and one of the largest in the world, and has 1,632,991 inhabitants in 2015, making it the fourth most populous city in the country and the most populous city proper in the entire Visayas-Mindanao region. 
				    		<br><br>
				    		<b>Davao Metropolitan Area</b>, the primary economic and urban build-up area in the region, is also <mark>the most populous in the island </mark>and the third most populous in the country, with about <b>2,274,913 inhabitants</b> in that year.
				    		<br><br>
							Most of the region's inhabitants speak <b>Cebuano language</b>. Tagalog and English are also spoken in schools, business, commerce, and industry. Prominent indigenous tribes like Bagobo, Manobo, and Mansaka also speak their own languages as well. Chinese immigrants are widespread in the region with a considerable population in Davao City. Davao Chinatown is one of the two defined chinatowns in the Philippines, Binondo being the other one. A considerable population of Japanese can also be seen in the country. Before World War II, Davao was heavily populated by Japanese immigrants. Davao City was <mark>the Little Tokyo of the Philippines</mark>.
							<br><br>
							
							
							<b>Fun fact:</b>
								
									Even though it is a part of Mindanao, <mark>the majority of the region's population composes of <b>Christians</b></mark>,
									mostly Catholics; there are also Muslims, Buddhists, and Shintoists living in the region.
								
							</p>
							</blockquote></blockquote>

						<blockquote>
						<h3 style="font-family: Ikaros; color: black; size: 48px;">ECONOMY</h3>
				    	<blockquote  style="text-align: justify;">
				    	<p>
				    		While the region’s economy is <mark>predominantly agri-based</mark>, it is now developing into a center for agro-industrial business, trade and tourism. Its competitive advantage is in agri-industry as its products, papayas, mangoes, bananas, pineapples, fresh asparagus, flowers, and fish products are exported internationally. The region can be a vital link to markets in other parts of Mindanao, Brunei Darussalam and parts of Malaysia and Indonesia. There is also a<mark> growing call center sector</mark> in the region, mostly centered in Davao City.<br><br>

							There is a gradual shift to industrialization as shown with industry’s <b>growth rate of 8.1%</b> in 1996. Other economic activities are <mark>mining, fishery, forestry and agriculture</mark>. Due to the region's rise as the main commercial and industrial hub of Mindanao, many of its workers are oriented to urban services such as putting small-scale businesses and working in commercial industries in thriving urban areas like Davao, Tagum, and Digos. Both private and foreign investors and businessmen are putting up huge business centers in the region, fueling up its commercial growth rate.<br><br>

							The region is also venturing to online business like <b>outsourcing</b>.
						</p>
