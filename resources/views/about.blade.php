				    	<h2 data-center="opacity: 1"  data-206-top="opacity: 1" data-anchor-target="#history h2">history</h2>
				    	<hr>
				    	<blockquote>
				    	<h3 style="font-family: Ikaros; color: black; size: 70px;">FIRST CONTACT WITH EUROPEANS</h3>
				    	<blockquote  style="text-align: justify;">
				    	
				    	<p data-center="opacity: 1"  data-206-top="opacity: 1"  data-anchor-target="#culture h2">
				    	The <b>Davao Gulf area</b> is <mark>the first region</mark> in the country that is in contact with the Europeans, with such contacts taking place as early as <b>16th century</b>. The <b>Portuguese</b> are the ones who preceded the Spaniards, who are the ones to colonize the region albeit much later, in sighting and visiting the region. In 1512, Francisco Serrano was shipwrecked in the shallow waters and coral reefs of Cape of San Agustín, located in what is now the province of Davao Oriental. In 1538, Francisco de Castro, a Portuguese captain, was driven by strong winds to the southeastern coast of Mindanao. He baptized several chieftains in the area.
				    	<br><br>
						Around January 1546, Francis Xavier, a Jesuit priest, left Malacca and went to Molucca Islands, then called the Spice Islands, where the Portuguese had some settlements, and for a year and a half he preached the Gospel to the inhabitants of Amboyna, Ternate, Baranura, and other lesser islands. It is claimed by some that during this expedition he landed on the island of Mindanao, which is confirmed by some writers of the seventeenth century, and in the Bull of canonization issued in 1623. It is also said that he is the one to have preached the Gospel in Mindanao.
						</p></blockquote></blockquote>

						<blockquote>
				    	<h3 style="font-family: Ikaros; color: black; size: 70px;">SPANISH ADMINISTRATION</h3>
				    	<blockquote  style="text-align: justify;">
				    	
				    	<p data-center="opacity: 1"  data-206-top="opacity: 1"  data-anchor-target="#culture h2">
				    	For centuries the tribes lived in relative peace until the Spanish, under the adventurous Spanish businessman Don Jose Oyanguren, arrived in the region in 1847. At that time, the Moro tribal chieftain <b>Datu Bago</b> was in control of the area in what is now Davao City. Don Oyanguren attempted to conquer the area which Datu Bago had ruled; although he failed at first, the Moro chieftain eventually evacuated his people to live in the areas near Mount Apo. This is the time the town of Davao, then called <b>Nueva Vergara</b> by the Spaniards, was established in the year 1848.
				    	<br><br>
						<mark>Don Oyanguren attempted to develop the region</mark>. Although the Spanish gained the upper hand when they finally controlled the ports of the region, the population of Davao grew very slowly until the arrival of Christian missionaries in the area in 1890. </p></blockquote></blockquote>

						<blockquote>
				    	<h3 style="font-family: Ikaros; color: black; size: 70px;">AMERICAN ADMINISTRATION</h3>
				    	<blockquote  style="text-align: justify;">
				    	
				    	<p data-center="opacity: 1"  data-206-top="opacity: 1"  data-anchor-target="#culture h2">
				    	After the Spanish–American War in 1898, Spanish rule in the region ended. Americans then landed in the region and they subsequently <mark>developed the regions communications and transportation systems</mark>. During this period, private farm ownership grew in the region. Japanese migration in the region began as two Japanese entrepreneurs, <b>Kyosaburo Otta</b> and <b>Yoshizo Furokawa</b>, were finding better agricultural lands for building abaca and coconut plantations in the region. <b>The Port of Davao</b> was opened on 1900, becoming <mark>the first Philippine international port to be established</mark> in the south.

						In 1903 until 1914, the region was one of the districts of the former Moro Province in Mindanao. After 1914, the province was replaced by an American colonial agency called <mark>Department of Mindanao and Sulu</mark>, which spanned the entire Mindanao island except Lanao. The agency lasted from 1914 to 1920.</p></blockquote></blockquote>

						<blockquote>
				    	<h3 style="font-family: Ikaros; color: black; size: 70px;">JAPANESE OCCUPATION</h3>
				    	<blockquote  style="text-align: justify;">
				    	
				    	<p data-center="opacity: 1"  data-206-top="opacity: 1"  data-anchor-target="#culture h2">
				    	In 1942, during World War II, as the Japanese occupation of the Philippines began, the region was <b>one of the first</b> among the Philippine regions to be subjected by Japanese occupation. The Japanese immigrants in Davao acted as a fifth column, welcoming the Japanese invaders during World War II. These Japanese were hated by the Moro Muslims and disliked by the Chinese. <b>The Moros</b> were judged as <mark>"fully capable of dealing with Japanese fifth columnists and invaders alike."</mark> The Moros were to fight the Japanese invaders when they landed at Davao on Mindanao. The Japanese went back to their ships at night to sleep since the Moros struck so much fear into them, even though the Moros were outnumbered by the Japanese. The longest battle of the Allied liberation campaign, <mark>the Battle of Davao</mark>, took place in <b>1945</b>. After the war, the region eventually passed to the American hands again for at least almost one year before the formal Philippine independence in July 4, 1946; most of the Japanese living in the region were now integrated in the Filipino population.</p></blockquote></blockquote>

				    	<blockquote>
				    	<h3 style="font-family: Ikaros; color: black; size: 70px;">PHILIPPINE ADMINISTRATION</h3>
				    	<blockquote  style="text-align: justify;">
				    	
				    	<p data-center="opacity: 1"  data-206-top="opacity: 1"  data-anchor-target="#culture h2">
				    	Even before the Philippine independence in 1946, the entire region was already a single province called <mark>Davao Province</mark>, with <b>Davao City serving as its capital</b>. The province was <b>one of the largest provinces in the Philippines during that time</b>, spanning more than <b>20,000 square kilometres</b> (7,700 sq mi). It lasted from 1920 until 1967, when the province was <b>split into three provinces</b> in May 1967: Davao del Norte, Davao del Sur and Davao Oriental. After the division, Davao City was officially named its regional center.</p></blockquote></blockquote>

